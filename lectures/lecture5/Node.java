public class Node{

    private String data;
    private Node next;

    public void setNext(Node N)
    {
        next = N;
    }
    public Node getNext()
    {
        return next;
    }
    public void setData(String D)
    {
        data = D;
    }
    public String getData()
    {
        return data;
    }
    

}